package com.guobao.dto.insure;

public class LyNBContState {

	private String prtNo;
	
	private String contNo;
	
	private String appFlag;
	
	private String uwFlag;
	
	private String isSignPol;
	
	private String bankCode;
	
	private String update_time;
	
	private String makeTime;

	public String getPrtNo() {
		return prtNo;
	}

	public void setPrtNo(String prtNo) {
		this.prtNo = prtNo;
	}

	public String getContNo() {
		return contNo;
	}

	public void setContNo(String contNo) {
		this.contNo = contNo;
	}

	public String getAppFlag() {
		return appFlag;
	}

	public void setAppFlag(String appFlag) {
		this.appFlag = appFlag;
	}

	public String getUwFlag() {
		return uwFlag;
	}

	public void setUwFlag(String uwFlag) {
		this.uwFlag = uwFlag;
	}
	
	public String getIsSignPol() {
		return isSignPol;
	}

	public void setIsSignPol(String isSignPol) {
		this.isSignPol = isSignPol;
	}

	public String getBankCode() {
		return bankCode;
	}

	public void setBankCode(String bankCode) {
		this.bankCode = bankCode;
	}

	public String getUpdate_time() {
		return update_time;
	}

	public void setUpdate_time(String update_time) {
		this.update_time = update_time;
	}

	public String getMakeTime() {
		return makeTime;
	}

	public void setMakeTime(String makeTime) {
		this.makeTime = makeTime;
	}
	
	
}
